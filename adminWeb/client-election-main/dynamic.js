  function voteInputEnter(voteInputs) {
    voteInputs.forEach((voteInput) => {
      voteInput.addEventListener("focus", (event) => {
        event.preventDefault();

        const instanceTd = voteInput.parentElement;
        const instanceTr = instanceTd.parentElement;
        const candidateId = instanceTr.children[0].id;
        let inputValue = 0;
        if (constData.length !== 0) {
          constData.forEach((candidate) => {
            if (candidate.candiId === candidateId) {
              inputValue = candidate.votes;
            }
          });
        }
        voteInput.value = inputValue;
      });

      voteInput.addEventListener("input", (event) => {
        event.preventDefault();
        const instanceTd = voteInput.parentElement;
        const instanceTr = instanceTd.parentElement;
        const candidateId = instanceTr.children[0].id;
        const inputValue = voteInput.value;

        if (inputValue !== "") {
          if (constData.length === 0) {
            constData.push({
              constituencyId: Number(globalConstituencyId),
              candiId: candidateId,
              votes: Number(inputValue),
              rsDecl: resDeclared,
            });
          } else {
            let index = constData.findIndex(
              (ele) => ele.candiId === candidateId
            );
            if (index !== -1) {
              constData.splice(index, 1);
            }
            constData.push({
              constituencyId: Number(globalConstituencyId),
              candiId: candidateId,
              votes: Number(inputValue),
              rsDecl: resDeclared,
            });
          }
        }
      });

      voteInput.addEventListener("keydown", (event) => {
        const { key } = event;
        const instanceTd = voteInput.parentElement;
        const instanceTr = instanceTd.parentElement;
        const nextTr = instanceTr.nextElementSibling;
        let nextVoteInput = null;

        if (nextTr) {
          const nextTd = nextTr.children[3];
          if (nextTd) {
            nextVoteInput = nextTd.children[0];
          }
        }

        if (key === "Enter" && document.activeElement.id === voteInput.id) {
          event.preventDefault();

          const input = voteInput.value;
          if (isValid(input, voteInput.parentElement.parentElement)) {
            if (nextVoteInput !== null) {
              nextVoteInput.focus();
            }
          }
        }
      });
    });
  }

  function isValid(input, parentEle) {
    if (isNaN(Number(input)) || Number(input) === null) {
      const tdElement = parentEle.children[2];
      const inputElement = tdElement.children[0];
      inputElement.style.border = "2px solid red";
      let errorMsg = parentEle.querySelector(".error-message");
      if (!errorMsg) {
        errorMsg = document.createElement("span");
        errorMsg.textContent = "Invalid! Use correct type";
        errorMsg.classList.add("error-message");
        parentEle.querySelector("td:last-child").appendChild(errorMsg);
      }
      return false;
    }
    return true;
  }

